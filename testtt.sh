Hardening serwera IIS 10.x
1. Cel zadania

Celem zadania jest zwiększenie poziomu bezpieczeństwa serwera IIS 10.x poprzez wdrożenie rekomendowanych ustawień hardeningowych zgodnych z najlepszymi praktykami bezpieczeństwa.

Hardening ma na celu:

ograniczenie powierzchni ataku (attack surface)

zabezpieczenie komunikacji HTTP/HTTPS

wyłączenie niepotrzebnych funkcjonalności

wdrożenie rekomendowanych nagłówków bezpieczeństwa

poprawę konfiguracji TLS

2. Zakres zadania

Zakres prac obejmuje:

Przegląd aktualnej konfiguracji IIS

Wdrożenie rekomendowanych ustawień bezpieczeństwa

Walidację konfiguracji

Dokumentację zmian

Hardening dotyczy:

serwera IIS 10.x

konfiguracji aplikacji webowych

konfiguracji protokołów TLS

nagłówków bezpieczeństwa HTTP
